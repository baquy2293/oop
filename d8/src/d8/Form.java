    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Date ngaysinh = null;
        er = "";
        
        if (t.getText().trim().isEmpty() || ns.getText().trim().isEmpty() || i.getText().trim().isEmpty()) {
            er += "nhap day du thogn tin";
        } else {
            try {
                ngaysinh = sp.parse(ns.getText());
            } catch (Exception e) {
                er += "ngay sinh khong hop le";
            }
        }

        if (er == "") {
            if (checkid(i.getText().trim())!=1 ) {
                String id = i.getText().trim(), tencauthu = t.getText().trim(), vitri = chon.getSelectedItem().toString();
                try {
                    File f = new File("cauthu.dat");
                    FileWriter fw = new FileWriter(f, true);
                    BufferedWriter bw = new BufferedWriter(fw);
                    bw.write(new CauThu(id, tencauthu, vitri, ngaysinh).toString());
                    clear();
                    bw.close();
                    fw.close();
                } catch (Exception e) {
                    System.out.println(e);
                }

            } else {
                JOptionPane.showMessageDialog(this, "da ton tai id chua mot cau thu");
            }

        } else {
            JOptionPane.showMessageDialog(this, er);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

