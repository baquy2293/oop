    ArrayList<CauThu> cauThus = new ArrayList<>();
    SimpleDateFormat sp = new SimpleDateFormat("dd/mm/yyyy");

    public void clear() {
        i.setText("");
        ns.setText("");
        t.setText("");

    }

    public int doc() {

        try {
            File f = new File("cauthu.dat");
            FileReader fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            String s;
            while ((s = br.readLine()) != null) {
                String[] tmp = s.split("\\,");
                cauThus.add(new CauThu(tmp[0], tmp[1], tmp[2], sp.parse(tmp[3])));
            }
            br.close();
            fr.close();

        } catch (Exception e) {
        }

        return -1;
    }

    public int checkname(String name) {
        cauThus.clear();
        doc();
        for (int j = 0; j < cauThus.size(); j++) {
            if (cauThus.get(j).getTen().equalsIgnoreCase(name)) {
                return j;
            }
        }
        return -1;
    }

    public int checkid(String id) {
        cauThus.clear();
        doc();
        for (CauThu cauThu : cauThus) {
            if (cauThu.getId().equalsIgnoreCase(id)) {
                return 1;
            }
        }
        return -1;
    }

