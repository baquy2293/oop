    public boolean checkId(String str) {
        try {

            File f = new File("giangvien.dat");
            FileReader fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            String s;
            while ((s = br.readLine()) != null) {
                String[] tmp = s.split("\\,");
                if (tmp[0].contains(str)) {
                    return false;
                }
            }
            br.close();
            fr.close();

        } catch (Exception e) {
        }

        return true;

    }


