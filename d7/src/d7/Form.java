    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String id, hoten, magiangvien, diachi, gioitinh, khoa;
        if (i.getText().trim().isEmpty() || ht.getText().trim().isEmpty()
                || mgv.getText().trim().isEmpty() || dc.getText().trim().isEmpty() || (!nam.isSelected() && !nu.isSelected())) {
            JOptionPane.showMessageDialog(this, " nhap day du thong tin");
        } else {
            id = i.getText();
            magiangvien = mgv.getText();
            diachi = dc.getText();
            gioitinh = nam.isSelected() ? "Nam" : "Nu";
            khoa = chon.getSelectedItem().toString();
            hoten = ht.getText();

            try {
                File f = new File("giangvien.dat");
                FileWriter fw = new FileWriter(f, true);
                BufferedWriter bw = new BufferedWriter(fw);
                if (checkId(i.getText().trim())) {
                    GiangVien gv = new GiangVien(id, hoten, magiangvien, diachi, gioitinh, khoa);
                    bw.write(gv.toString());
                    clear();
                } else {
                    JOptionPane.showMessageDialog(this, " id da ton tai");
                }

                bw.close();
                fw.close();

            } catch (Exception e) {
            }

        }


    }//GEN-LAST:event_jButton2ActionPerformed

